#include <unistd.h>

int main(int c,char **v)
{
    if (c != 3)
    {
        write(1, "\n", 1);
        return 0;
    }
    int i = 0;
    int j = 0;
    while(v[2][j])
    {
        if (v[2][j] == v[1][i])
        {
            i++;
        }
        j++;
    }
    if (v[1][i] == '\0')
        write(1, "1", 1);
    else
        write(1 ,"0", 1);
    write(1, "\n", 1);
    return 0;
}
