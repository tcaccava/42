game = PongGameState()  # single global game instance (one room)


@asynccontextmanager
async def lifespan(app: FastAPI):
    """
    FastAPI lifespan context manager.
    Starts the world loop as a background task on server startup,
    cancels it cleanly on shutdown.
    """
    task = asyncio.create_task(world_loop())    # start game loop in background
    yield                                       # server runs here
    task.cancel()                               # cancel loop on shutdown


app = FastAPI(lifespan=lifespan)    # FastAPI instance with lifespan handler


async def world_loop():
    """
    Main game loop. Runs at 60fps.
    On each iteration:
      1. If game is playing, advance physics by one tick
      2. Serialize current game state to JSON string (once, reused for all clients)
      3. Broadcast to all connected clients concurrently using asyncio.gather
      4. Sleep for the remaining time to maintain 60fps cadence
    Serializing once and reusing the string avoids redundant work when
    there are multiple connected clients.
    """
    loop_time = 1 / 60  # target interval in seconds (~16.6ms)

    while True:
        start = time.time()                         # record tick start time

        if game.status == "playing":
            game.update_physics()                   # advance ball and paddles

        payload_json = json.dumps(game.get_payload())   # serialize once for all clients

        if game.connections:
            await asyncio.gather(                   # send to all clients concurrently
                *[ws.send_text(payload_json) for ws in list(game.connections)]  # positional unpacking + list comprehension unpacked into gather: send_text is an awaitable func,so if you call it without await it return a coroutine object. The list comprehension build a list of this coroutines,and the * unpacks them as arguments for gather,which executes them concurrently.
            )

        wait = loop_time - (time.time() - start)    # remaining time in this tick
        await asyncio.sleep(max(0, wait))           # sleep, clamped to 0 to avoid negative sleep


async def send_to(ws: WebSocket, type: str, payload: dict):
    """
    Send a single typed JSON message to one client.
    Wraps the message in the standard { type, payload } envelope.
    Used for one-off messages (playerAssigned, error, etc.) as opposed
    to the broadcast in world_loop which uses send_text directly.
    """
    await ws.send_text(json.dumps({"type": type, "payload": payload}))


async def broadcast(type: str, payload: dict):
    """
    Utility: send a single typed JSON message to ALL connected clients.
    Used for events that all clients need to receive simultaneously,
    such as lobbyUpdate — as opposed to send_to which targets one client.
    Reuses the same serialized string for all clients to avoid redundant work.
    """
    msg = json.dumps({"type": type, "payload": payload})    # serialize once for all recipients
    if game.connections:
        await asyncio.gather(                               # send concurrently to all clients
            *[ws.send_text(msg) for ws in list(game.connections)]  # list comprehension unpacked into gather
        )


@app.websocket("/ws")
async def websocket_endpoint(ws: WebSocket):
    """
    WebSocket connection handler. One coroutine instance runs per connected client.
    Accepts the connection, adds it to the global connections set,
    then loops waiting for messages.
    'player' starts as None and is assigned when joinLobby is received —
    initialized here so the except block can safely reference it even if
    the client disconnects before sending joinLobby.
    On disconnect or any exception, marks the player as disconnected,
    broadcasts the updated lobby state to remaining clients and
    removes the socket from the connections set.
    """
    await ws.accept()               # complete WebSocket handshake
    game.connections.add(ws)        # register connection for broadcasts
    player: Optional[Player] = None # will be assigned on joinLobby. Same of Union[Player, None] = None

    try:
        while True:
            data = await ws.receive_json()      # wait for next message, parsed as dict
            msg = ClientMessage(**data)         # validate envelope with Pydantic

            if msg.type == "joinLobby":
                parsed = JoinLobbyPayload(**msg.payload)    # validate joinLobby-specific payload
                player = game.get_free_slot()               # find first available slot
                if player:
                    player.connected = True                 # mark slot as occupied
                    player.name = parsed.playerName         # set display name
                    await send_to(ws, "playerAssigned", {   # confirm assignment to this client
                        "playerId": player.id,
                        "side": player.side,
                        "name": player.name,
                        "roomId": "main",
                        "gameStatus": game.status,
                    })
                    await broadcast("lobbyUpdate", game.get_lobby_state())  # notify ALL clients of updated lobby
                else:
                    await send_to(ws, "error", {"message": "Lobby full", "code": "LOBBY_FULL"})

            elif msg.type == "startGame":
                parsed = StartGamePayload(**msg.payload)    # validate startGame payload
                game.status = "playing"                     # transition game to playing state

            elif msg.type == "move":
                parsed = MovePayload(**msg.payload)         # validate move payload
                if player:
                    player.current_direction = parsed.direction         # saves current direction (None = stop),so the functio move read the direction and moves the paddle until None arrives: this guarantees smooth paddle movement
                    player.last_processed_input_id = parsed.inputId     # record for reconciliation echo

            elif msg.type == "voteAbandon":
                if player:
                    player.votedToAbandon = True            # flag this player's abandon vote

            elif msg.type == "restartGame":
                pass                                        # not yet implemented

    except (WebSocketDisconnect, Exception):
        if player:
            player.connected = False            # free up the slot
            player.current_direction = None     # stop any ongoing movement
        game.connections.discard(ws)            # discard (unlike remove, doesn't raise error if not present): first discard
        await broadcast("lobbyUpdate", game.get_lobby_state()) # then broadcast remaining clients that a player left