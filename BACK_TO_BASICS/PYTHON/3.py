class MovePayload(BaseModel):
    """
    Payload for a 'move' message sent by the client when a key is pressed or released.
    Direction is Optional because the client sends null on key release (stop movement).
    inputId is a increasing integer generated by the client,specific for that client,
    used for server reconciliation — the server echoes it back so the client
    knows which inputs have been processed.
    """
    direction: Optional[Literal["UP", "DOWN"]] = None  # "UP", "DOWN", or None (stop). It's the same of Union[Literal["UP","DOWN"], None] = None
    inputId: int                                        # client-side counter, starts at 0, resets on game start


class JoinLobbyPayload(BaseModel):
    """Payload for a 'joinLobby' message. Contains the player's chosen name."""
    playerName: str


class StartGamePayload(BaseModel):
    """Payload for a 'startGame' message. vsAI flag defaults to False."""
    vsAI: bool = False  # if True, one slot is controlled by AI (if we want to implement that)


class ClientMessage(BaseModel):
    """
    Envelope for all messages received from the client.
    Every WebSocket message must have a 'type' field matching one of the
    accepted literals, and an optional 'payload' dict.
    The payload is kept as a raw dict here and validated separately inside
    each branch of the message handler, because each type has a different schema.
    """
    type: Literal["move", "joinLobby", "startGame", "voteAbandon", "restartGame", "spectate"]  # accepted message types
    payload: dict = {}  # raw dict, validated per-type in the handler


