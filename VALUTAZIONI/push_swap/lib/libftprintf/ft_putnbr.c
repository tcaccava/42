#include "ft_printf.h"

void	ft_putnbr(int n, int *l)
{
	if (n == INT_MIN)
	{
		ft_putstr("-2147483648", l);
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-', l);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr((n / 10), l);
	ft_putchar('0' + (n % 10), l);
}
