#include <unistd.h>

void	ft_putstr(const char *str, int *l)
{
	int	i;

	if (!str)
	{
		write(1, "(null)", 6);
		*l += 6;
		return ;
	}
	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
		(*l)++;
	}
}
