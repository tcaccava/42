#include <unistd.h>

void	ft_putnbr(int nbr, int *l)
{
	if (nbr == -2147483648)
	{
		write(1, "-2147483648", 11);
		*l += 11;
		return ;
	}
	if (nbr < 0)
	{
		write(1, "-", 1);
		(*l)++;
		ft_putnbr(nbr * -1, l);
	}
	else
	{
		if (nbr > 9)
			ft_putnbr(nbr / 10, l);
		write(1, &(char){nbr % 10 + '0'}, 1);
		(*l)++;
	}
}
